# Hetzner Cloud provisioner
# ────────────────────────────────────────────────────────────────

_CLOUD_INIT_TEMPLATE = """#cloud-config
# Nexus Platform on Hetzner Cloud — cloud-init user-data.
# Generated by Console for instance {instance_id} ({instance_name}).

package_update: true
package_upgrade: true

packages:
  - docker.io
  - docker-compose-v2

write_files:
  - path: /opt/nexus/docker-compose.yml
    permissions: '0644'
    content: |
{compose_indented}
  - path: /opt/nexus/.env
    permissions: '0600'
    content: |
      # Values below are placeholders — the provisioner MUST rewrite this file
      # with the real secrets from nexus.secrets.env before starting Compose.
      PLATFORM_BOOTSTRAP_TOKEN=${{PLATFORM_BOOTSTRAP_TOKEN}}
      POSTGRES_PASSWORD=${{POSTGRES_PASSWORD}}
      ANTHROPIC_API_KEY=${{ANTHROPIC_API_KEY}}

runcmd:
  - systemctl enable --now docker
  - cd /opt/nexus && docker compose up -d
"""


def provision_hetzner(
    instance_id: UUID,
    instance_name: str,
    region: str | None = None,
    domain: str | None = None,
) -> tuple[Path, str, PlaybookInputs]:
    """Write cloud-init + docker-compose.yml + return playbook inputs."""
    dir_ = _instance_dir(instance_id)
    token = secrets.token_urlsafe(32)
    resolved_region = region or "fsn1"  # Falkenstein, EU-central
    server_name = f"nexus-{_short_id(instance_id)}"

    _write_compose(dir_)
    _write_env_placeholders(dir_, token)

    compose_body = (dir_ / "docker-compose.yml").read_text()
    compose_indented = "\n".join(f"      {line}" for line in compose_body.splitlines())

    cloud_init = _CLOUD_INIT_TEMPLATE.format(
        instance_id=instance_id,
        instance_name=instance_name,
        compose_indented=compose_indented,
    )
    (dir_ / "cloud-init.yaml").write_text(cloud_init, encoding="utf-8")

    inputs = PlaybookInputs(
        instance_id=instance_id,
        instance_name=instance_name,
        modality="hetzner",
        region=resolved_region,
        domain=domain,
        deployments_dir=dir_,
        endpoint_hint=domain or f"http://{server_name}.hetzner.local:8000",
        required_secrets=[
            "HCLOUD_TOKEN",
            "PLATFORM_BOOTSTRAP_TOKEN",
            "POSTGRES_PASSWORD",
            "ANTHROPIC_API_KEY",
            "SSH_KEY_NAME",
        ],
        provider_steps=[
            {
                "title": "Export Hetzner Cloud token",
                "cmd": "export HCLOUD_TOKEN=${HCLOUD_TOKEN}",
            },
            {
                "title": "Rewrite the .env in cloud-init with real secrets",
                "cmd": (
                    "envsubst < ./cloud-init.yaml > ./cloud-init.resolved.yaml"
                ),
            },
            {
                "title": "Create the server (cx22 = 2 vCPU / 4 GB RAM / 40 GB, ~€4.15/mo)",
                "cmd": (
                    f"hcloud server create --name {server_name} --image ubuntu-24.04 "
                    f"--type cx22 --location {resolved_region} --ssh-key ${{SSH_KEY_NAME}} "
                    f"--user-data-from-file ./cloud-init.resolved.yaml"
                ),
            },
            {
                "title": "Attach a floating IP (optional but recommended for stable DNS)",
                "cmd": (
                    f"hcloud floating-ip create --type ipv4 --home-location {resolved_region} "
                    f"--name {server_name}-ip && "
                    f"hcloud floating-ip assign {server_name}-ip {server_name}"
                ),
            },
            {
                "title": "Wait for cloud-init to finish and Compose to boot",
                "cmd": (
                    f"hcloud server ssh {server_name} 'cloud-init status --wait && "
                    f"docker compose -f /opt/nexus/docker-compose.yml ps'"
                ),
            },
        ],
        post_deploy_checks=[
            f"hcloud server ssh {server_name} 'curl -fsSL http://localhost:8000/_health'",
            f"hcloud server ssh {server_name} 'curl -fsSL http://localhost:8000/_voice/health'",
        ],
    )
    return dir_, token, inputs


# ────────────────────────────────────────────────────────────────
# Helpers
# ────────────────────────────────────────────────────────────────

def _instance_dir(instance_id: UUID) -> Path:
    d = settings.deployments_dir / str(instance_id)
    d.mkdir(parents=True, exist_ok=True)
    return d


def _short_id(instance_id: UUID) -> str:
    return str(instance_id)[:8]


def _write_compose(dir_: Path) -> None:
    body = _COMPOSE_TEMPLATE.format(
        image=settings.local_platform_image,
        port=8000,
    )
    (dir_ / "docker-compose.yml").write_text(body, encoding="utf-8")


def _write_env_placeholders(dir_: Path, token: str) -> None:
    """Write a `.env.example` — real values live in `nexus.secrets.env`.

    We DO NOT write a real `.env`. That's the whole point of the playbook
    handoff — secrets stay out of Console's disk.
    """
    (dir_ / ".env.example").write_text(
        "# Populate these from nexus.secrets.env before starting Compose.\n"
        f"PLATFORM_BOOTSTRAP_TOKEN={token}  # generated fresh per instance\n"
        "POSTGRES_PASSWORD=change-me\n"
        "ANTHROPIC_API_KEY=change-me\n",
        encoding="utf-8",
    )
